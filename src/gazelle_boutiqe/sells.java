    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=(Connection)DriverManager.getConnection("jdbc:mysql://localhost/gezele", "root","");
            String sql="SELECT *from customer where id=? ";
            String sql2="SELECT *FROM special_customer WHERE id=?";

            //Date selectDate= date.getDate();
            //java.sql.Date sqlDate= new java.sql.Date(selectDate.getTime());
            String date1=date.getText();
            String date2=date.getText();

            PreparedStatement ptst=conn.prepareStatement(sql);
            PreparedStatement ptst1=conn.prepareStatement(sql2);
            ptst.setString(1, date1 );
            ptst1.setString(1, date2 );
            
            ResultSet rs=ptst.executeQuery();
            ResultSet rs2=ptst1.executeQuery();

            
            //for using correctly the table we have to use the referance of that as bellow
            DefaultTableModel tm=(DefaultTableModel)sells1.getModel();
            tm.setRowCount(0);

            while(rs.next()){
                Object o[]={rs.getString("name_Lname"),rs.getString("phone"),
                            rs.getDate("date")        ,rs.getString("id"),       
                            rs.getString("pro_name")  ,rs.getInt("price"), 
                            rs.getInt("discount")     ,rs.getInt("num_item"),
                            rs.getBoolean("gift")};
                            
                tm.addRow(o);
            }
            while(rs2.next()){
                Object o[]={rs2.getString("name_Lname"),rs2.getString("phone"),
                            rs2.getDate("order_date")  ,rs2.getString("id"), 
                            rs2.getString("pro_name")  ,rs2.getInt("price"),
                            rs2.getInt("discount")     ,rs2.getInt("num_item"),
                            rs2.getBoolean("gift")};
                tm.addRow(o);
            }

        }catch( Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

