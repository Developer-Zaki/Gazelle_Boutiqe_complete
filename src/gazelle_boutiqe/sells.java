    private void total_priceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_total_priceActionPerformed
        // TODO add your handling code here:
        int price=0;
        int num_item=0;
        int disc=0;
        int value2;
        int value;
        int finalSum=0;
        int row=(int) sells1.getRowCount();
        int row2=(int) sells1.getRowCount();
        for(int i=0; i<row; i++){
            value=(int) sells1.getValueAt(i, 5);
            price +=value;
        }
        for(int j=0; j<row2; j++){
            value2=(int) sells1.getValueAt(j, 7);
            num_item +=value2;
        }
        for(int j=0; j<row2; j++){
            value2=(int) sells1.getValueAt(j, 6);
            disc +=value2;
        }
        finalSum=(num_item*price)-disc;
        price1.setText("  "+ finalSum);
    }//GEN-LAST:event_total_priceActionPerformed

